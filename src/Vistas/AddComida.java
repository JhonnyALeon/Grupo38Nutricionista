    private void jbBuscarComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarComidaActionPerformed
        if (!jtNombre.getText().equals("")){
            String nombre=jtNombre.getText();
            ComidaData cd= new ComidaData();
            Comida comida=cd.BuscarComidaNombre(nombre);
            jlNombre.setText(comida.getNombre());
            jlCalorias.setText(""+comida.getCalorias());
            jlDetalle.setText(comida.getDetalle());
            jlCodigo.setText(""+comida.getIdComida());
        }else if(!jtCodigo.getText().equals("")){
            int id =Integer.parseInt(jtCodigo.getText());
            ComidaData cd= new ComidaData();
            Comida comida=cd.BuscarComidaID(id);
            jlNombre.setText(comida.getNombre());
            jlCalorias.setText(""+comida.getCalorias());
            jlDetalle.setText(comida.getDetalle());
            jlCodigo.setText(jtCodigo.getText());
        }else{
            JOptionPane.showMessageDialog(null, "Debe llenar al menos un campo");
        }
    }//GEN-LAST:event_jbBuscarComidaActionPerformed

